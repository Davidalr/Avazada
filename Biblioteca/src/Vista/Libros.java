    private void llenarLibros() {
        ArrayList<Libro> Libros = LibroController.getInstance().getLibroArray();
        listLibros.setListData(Libros.toArray());
    }

    public void llenarDatos() {
        ArrayList<Negocio.Materia> materias = LibroController.getInstance().materias();
        DefaultComboBoxModel comboModel = new javax.swing.DefaultComboBoxModel(materias.toArray());
        materiaTxt.setModel(comboModel);

        ArrayList<Negocio.Autor> autores = (ArrayList) LibroController.getInstance().autores();
        DefaultComboBoxModel comboModel1 = new javax.swing.DefaultComboBoxModel(autores.toArray());
        autorTxt.setModel(comboModel1);
    }
