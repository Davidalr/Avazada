    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        HashMap hashMap = new HashMap();
        hashMap.put("titulo", tituloTxt.getText());
        hashMap.put("isbn", isbnTxt.getText());
        hashMap.put("autor", autorTxt.getSelectedItem());
        hashMap.put("materia", materiaTxt.getSelectedItem());
        hashMap.put("numero_ejemplares", Integer.parseInt(ejemplaresTxt.getText()));
        hashMap.put("numero_prestamos", Integer.parseInt(prestamosTxt.getText()));
        LibroController.getInstance().llenarLibro(hashMap);
        llenarLibros();   // TODO add your handling code here:


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        HashMap hashMap = new HashMap();
        Negocio.Libro libro = (Negocio.Libro) listLibros.getSelectedValue();
        hashMap.put("codigo", libro.getId());
        hashMap.put("titulo", tituloTxt.getText());
        hashMap.put("isbn", isbnTxt.getText());
        hashMap.put("autor", autorTxt.getSelectedItem());
        hashMap.put("materia", materiaTxt.getSelectedItem());
        hashMap.put("numero_ejemplares", Integer.parseInt(ejemplaresTxt.getText()));
        hashMap.put("numero_prestamos", Integer.parseInt(prestamosTxt.getText()));
        LibroController.getInstance().actualizarLibro(libro, hashMap);
        llenarLibros();
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

    private void listLibrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listLibrosMouseClicked
        // TODO add your handling code here:
        Libro libro = (Libro) listLibros.getSelectedValue();
        tituloTxt.setText(libro.getTitiulo());
        isbnTxt.setText(libro.getIsbn());
        autorTxt.setSelectedItem(libro.getAutor());
        materiaTxt.setSelectedItem(libro.getMateria());
        ejemplaresTxt.setText(Integer.toString(libro.getNumeroEjemplares()));
        prestamosTxt.setText(Integer.toString(libro.getNumeroPrestamos()));
    }//GEN-LAST:event_listLibrosMouseClicked


